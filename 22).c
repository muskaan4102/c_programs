#include<stdio.h>
/*from video 4: Converting the number in different number system*/
int main()
{
	int num;
	num = 34245;						//decimal number 
	printf("The number is :%d",num );
	num = 055;							//octal number
	printf("\nThe number is :%d",num );
	num = 0x43A;						//hexadecimal number
	printf("\nThe number is :%d",num );
	return 0;
}