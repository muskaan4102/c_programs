#include<stdio.h>
/*Set 2: Storing the value of c and d at two different locations*/
int main()
{
	int c ,d;
	printf("\tEnter value of c: ");
	scanf("%d",&c );
	printf("\tEnter value of d: ");
	scanf("%d",&d );
	printf("\tThe value of c is %d and the value of d is %d",d,c );
	printf("\nHence the value of the number is %d%d",d,c);
	return 0;

}	